public class SwitchT1 {
    public static void main(String[] args) {
        int i = 10;
        switch (i){
            case 5:
                System.out.println("555");
                break;
            case 10:
                System.out.println("101010");
                break;
            case 15:
                System.out.println("151515");
                break;
            default:
                System.out.println("Default case");
        }
        }
    }
